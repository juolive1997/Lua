-- Declaração de variável e atribuição de valor
-- e atribuição de valor através do sinal de =
local texto = "Hello World"
print( texto )

local texto2 = "Seja bem vindo"
print( texto2)

-- concatenar ( juntar ) variaveis/valores através dos .. (dois pontos)
print( texto..texto2 )

print (texto .. " " .. texto2 )

print ("Frase 1: " .. texto .. " Frase 2: " .. texto2 )
